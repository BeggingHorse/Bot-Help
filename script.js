    const pluginTitle = document.createElement("h2");
    pluginTitle.textContent = plugin.name; // ä¸æ˜¾ç¤ºåŠŸèƒ½æ•°é‡
    pluginCard.appendChild(pluginTitle);

    const functionList = document.createElement("ul");
